#include "libft.h"

int		ft_sqrt(int nb)
{
	int a;

	if (nb <= 0)
		return (0);
	a = 1;
	while (nb != a * a)
	{
		a++;
		if (a > nb / a)
			return (0);
	}
	if (nb == a * a)
		return (a);
	return (0);
}
